        [Л.1] 25.IV-45 г.Пишу на трофейной бумаге.Дорогие, милые, славные жинка, доченька, бабушка и друзья!22.IV с.г. части доблестной Красной Армии при взятии гор. Люкенвальде освободили наш интернациональный лагерь военнопленных. Красные бойцы в один раз вырвали из лап фашистов 17.000 человек и из бесправных, обездоленных, голодных мы опять стали полноправными людьми. Писать о всех ужасах плена нет никакой возможности, т.к. на это не хватит ни времени, ни бумаги. Знайте, что это был сплошной злой кошмар, о котором можно говорить целыми годами. Погибло в плену уйма народа. Никакая холера, тиф и мор не погубили столько народу, сколько злой немецко-гитлеровский режим. Нас за людей не считали, а называли «унтеменшами» и скотами.Попал я в плен 4.Х-41 г. под деревней Леоново, Ельнинский р-н Смоленской обл.Враг 2.Х обрушился на нас со всей своей тяжестью. 4.Х я получил три раненая и 2 контузии. Я был без сознания. Очнулся я в коровнике, в навозе еще с несколькими ранеными солдатами. После долгих мытарств, страданий меня вместе с другими доставили в Вильно. В ране кишели черви. Ко всем моим ужасам я еще заболел сыпным тифом. Всего в госпитале пролежал до 20.V-42 г. Из Вильно меня вместе с другими отвезли в Ковно, где оставался в карантинном лагере до 2.VI. 6.VI нашу партию пленных привезли в этот проклятый город Люкенвальде. Если есть ад, то это плен, и находится он в Люкенвальде. Теперь мы скоро увидимся и заживем по-старому дружно и хорошо. Окончание войны не за горами. Наша родная Красная Армия так лупит врага, что он нигде не может устоять, бежит и в панике не знает, где укрепиться.Когда смотришь на наших бойцов, то от радости думаешь сердце лопнет, прилив счастья вызывает слезы радости. Обидно только, что мне не пришлось воевать в одних рядах с ними, а сидел в плену. Теперь я вновь возьму винтовку и пойду добивать этого гада. Когда покончим с ним, то все вернемся домой к мирному труду восстановления всего народного хозяйства.Дорогие жинка и доченька! Не было дня, часа и минуты, чтобы я не вспоминал вас. Когда мне было тяжело, я обращался к вам, когда меня били, я просил у вас защиты. Связь с вами душевная спасла меня и я опять вновь могу быть с вами. Где мои братья и живы ли они? Где вы? Хочется хоть одним глазом, на одну секунду увидеть вас и прижать к сердцу.Моей фабрике привет. Пусть во славу нашей гордости тов. Сталина работают еще лучше.Целую многоГеннадий.По этому адресу не пишите. Со временем пришлю настоящий, постоянный.

